package modelosExcel;

public class VehiculoExcel {
    private String nifPropietario;
    private String tipoVehiculo;
    private String marca;
    private String modelo;
    private String matricula;
    private String bastidor;
    private String unidadCobro;
    private double valorUnidad;
    private double importe;
    private double exenc_bonif;
    private double total;

    public String getNifPropietario() {
        return nifPropietario;
    }

    public void setNifPropietario(String nifPropietario) {
        this.nifPropietario = nifPropietario;
    }

    public String getTipoVehiculo() {
        return tipoVehiculo;
    }

    public void setTipoVehiculo(String tipoVehiculo) {
        this.tipoVehiculo = tipoVehiculo;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getMatricula() {
        return matricula;
    }

    public void setMatricula(String matricula) {
        this.matricula = matricula;
    }

    public String getBastidor() {
        return bastidor;
    }

    public void setBastidor(String bastidor) {
        this.bastidor = bastidor;
    }

    public String getUnidadCobro() {
        return unidadCobro;
    }

    public void setUnidadCobro(String unidadCobro) {
        this.unidadCobro = unidadCobro;
    }

    public double getValorUnidad() {
        return valorUnidad;
    }

    public void setValorUnidad(double valorUnidad) {
        this.valorUnidad = valorUnidad;
    }

    public double getImporte() {
        return importe;
    }

    public void setImporte(double importe) {
        this.importe = importe;
    }

    public double getExenc_bonif() {
        return exenc_bonif;
    }

    public void setExenc_bonif(double exenc_bonif) {
        this.exenc_bonif = exenc_bonif;
    }

    public double getTotal() {
        return total;
    }

    public void setTotal(double total) {
        this.total = total;
    }
}

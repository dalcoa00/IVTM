package modelosExcel;

public class ContribuyenteExcel {
    private Integer idContribuyente;
    private String nombre;
    private String apellido1;
    private String apellido2;
    private String nifnie;
    private String direccion;
    private String numero;
    private String paisCcc;
    private String ccc;
    private String iban;
    private String email;
    private Double bonificacion;
    private String aytoCont;

    public Integer getIdContribuyente() {
        return idContribuyente;
    }

    public void setIdContribuyente(Integer idContribuyente) {
        this.idContribuyente = idContribuyente;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido1() {
        return apellido1;
    }

    public void setApellido1(String apellido1) {
        this.apellido1 = apellido1;
    }

    public String getApellido2() {
        return apellido2;
    }

    public void setApellido2(String apellido2) {
        this.apellido2 = apellido2;
    }

    public String getNifnie() {
        return nifnie;
    }

    public void setNifnie(String nifnie) {
        this.nifnie = nifnie;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getNumero() {
        return numero;
    }
    public void setNumero(String numero) {
        this.numero = numero;
    }
    public String getPaisCcc() {
        return paisCcc;
    }
    public void setPaisCcc(String paisCcc) {
        this.paisCcc = paisCcc;
    }
    public String getCcc() {
        return ccc;
    }
    public void setCcc(String ccc) {
        this.ccc = ccc;
    }

    public String getIban() {
        return iban;
    }

    public void setIban(String iban) {
        this.iban = iban;
    }

    public String getEmail() { return email; }

    public void setEmail(String email) { this.email = email; }

    public Double getBonificacion() {
        return bonificacion;
    }

    public void setBonificacion(Double bonificacion) {
        this.bonificacion = bonificacion;
    }

    public String getAytoCont() {
        return aytoCont;
    }

    public void setAytoCont(String aytoCont) {
        this.aytoCont = aytoCont;
    }
}
